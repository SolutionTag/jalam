package com.meshyog.hydra.enity.distributor;

public class WCanStockInfo {

	private Long canId;
	private String canName;
	private String canQuantity;
	private String canPrice;
	private String canManufactureName;
	private String canManufactureLocation;
	private String canExpiryDate;
	public Long getCanId() {
		return canId;
	}
	public void setCanId(Long canId) {
		this.canId = canId;
	}
	public String getCanName() {
		return canName;
	}
	public void setCanName(String canName) {
		this.canName = canName;
	}
	public String getCanQuantity() {
		return canQuantity;
	}
	public void setCanQuantity(String canQuantity) {
		this.canQuantity = canQuantity;
	}
	public String getCanPrice() {
		return canPrice;
	}
	public void setCanPrice(String canPrice) {
		this.canPrice = canPrice;
	}
	public String getCanManufactureName() {
		return canManufactureName;
	}
	public void setCanManufactureName(String canManufactureName) {
		this.canManufactureName = canManufactureName;
	}
	public String getCanManufactureLocation() {
		return canManufactureLocation;
	}
	public void setCanManufactureLocation(String canManufactureLocation) {
		this.canManufactureLocation = canManufactureLocation;
	}
	public String getCanExpiryDate() {
		return canExpiryDate;
	}
	public void setCanExpiryDate(String canExpiryDate) {
		this.canExpiryDate = canExpiryDate;
	}
	
}
